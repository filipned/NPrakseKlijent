    private void popuniCBBranse() {
        List<AbstractObject> branse = new ArrayList<>();
        
        try {
            branse = Controler.getInstance().ucitajSveBranse();
            
        } catch (IOException ex) {
            Logger.getLogger(PodaciKompanijaForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PodaciKompanijaForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UcitavanjeBransiException ex) {
            JOptionPane.showMessageDialog(this, "Greska pri ucitavanju bransi!");
            Logger.getLogger(PodaciKompanijaForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for (AbstractObject o : branse) {
            jComboBoxBransa.addItem((Bransa)o);
        }
    }

    private void ocistiPolja() {
        jTextFieldRegBr.setText("");
        jTextFieldSifraDjelatnosti.setText("");
        jTextFieldPoslovnoIme.setText("");
        jTextFieldKontakt.setText("");
        lm.removeAllElements();
    }

    private Kompanija vratiKompaniju() {
        String regBr = jTextFieldRegBr.getText();
        String sifraDjelatnosti = jTextFieldSifraDjelatnosti.getText();
        String poslovnoIme = jTextFieldPoslovnoIme.getText();
        String kontakt = jTextFieldKontakt.getText();
        List<Bransa> branse = new ArrayList<>();
        for (int i = 0; i < lm.size(); i++) {
            branse.add((Bransa) lm.get(i));
            
        }
        
        return validirajIVrati(regBr, sifraDjelatnosti, poslovnoIme, kontakt, branse);
    
    }

    private Kompanija validirajIVrati(String regBr, String sifraDjelatnosti, String poslovnoIme, String kontakt, List<Bransa> branse) {
        Kompanija kompanija = null;
        if(regBr.isEmpty()){
            JOptionPane.showMessageDialog(this, "Unesite registarski broj!");
        } else if(sifraDjelatnosti.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite sifru djelatnosti!");
        } else if(poslovnoIme.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite poslovno ime!");
        } else if(kontakt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite kontakt!");
        } else if((branse == null || branse.isEmpty()) && !tipUnosa.equals("UPDATE")) {
            JOptionPane.showMessageDialog(this, "Dodajte branse!");
        } else {
            kompanija = new Kompanija(0, regBr, sifraDjelatnosti, poslovnoIme, kontakt, branse);
        }
        return kompanija;
    }
