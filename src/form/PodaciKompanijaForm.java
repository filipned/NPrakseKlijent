    private void jButtonSacuvajKompanijuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajKompanijuActionPerformed
        // TODO add your handling code here:
        Kompanija kompanija = vratiKompaniju();
        
       
        if(kompanija != null) {
            try {
                if(tipUnosa == "INSERT") {
                    Controler.getInstance().unesiNovuKompaniju(kompanija);
                    JOptionPane.showMessageDialog(this, "Uspesno sacuvana kompanija");
                    ocistiPolja();
                    mainForm.srediKompanijaTabelu();
                } else {
                    if(kompanija != null) {
                    kompanija.setKompanijaID(kompanijaZaIzmjenu.getKompanijaID());
                    Controler.getInstance().azurirajKompaniju(kompanija);
                    JOptionPane.showMessageDialog(this, "Uspesno azurirana kompanija");
                    dispose();
                    mainForm.srediKompanijaTabelu();
                }
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(PodaciKompanijaForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska pri unosu kompanije!");
                Logger.getLogger(PodaciKompanijaForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButtonSacuvajKompanijuActionPerformed

    private void jButtonDodajBransuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajBransuActionPerformed
        // TODO add your handling code here:
        
        Bransa bransa = (Bransa) jComboBoxBransa.getSelectedItem();
        if(bransa == null) {
            JOptionPane.showMessageDialog(this, "Odaberite bransu!");
            return;
        }
        if(lm.contains(bransa)) {
            JOptionPane.showMessageDialog(this, "Bransa je vec unijeta!");
            return;
        }
        lm.addElement(bransa);
        jListBranse.setModel(lm);
    }//GEN-LAST:event_jButtonDodajBransuActionPerformed

