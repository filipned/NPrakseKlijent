    private void popuniCBFakulteti() {
        List<AbstractObject> fakulteti = new ArrayList<>();
        try {
            fakulteti = Controler.getInstance().ucitajSveFakultete();
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Greska pri ucitavanju fakulteta!");
            Logger.getLogger(PodaciStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PodaciStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UcitavanjeFakultetaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            this.dispose();
            Logger.getLogger(PodaciStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for (AbstractObject fakultet : fakulteti) {
            jComboBoxFakulteti.addItem((Fakultet) fakultet);
        }
    }

    private Student vratiStudenta() {
        String ime = jTextFieldImeStudenta.getText().trim();
        String prezime = jTextFieldPrezimeStudenta.getText().trim();
        String godinaUpisa = jTextFieldGodinaUpisa.getText().trim();
        String brIndeksa = jTextFieldBrIndeksa.getText().trim();
        Fakultet fakultet = (Fakultet) jComboBoxFakulteti.getSelectedItem();
        
        return validirajIVrati(ime, prezime, brIndeksa,godinaUpisa, fakultet);
    }

    private Student validirajIVrati(String ime, String prezime, String brIndeksa, String godinaUpisa, Fakultet fakultet) {
        Student student = null;

        if(ime.isEmpty()){
            JOptionPane.showMessageDialog(this, "Unesite ime!");
        } else if(prezime.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite prezime!");
        } else if(godinaUpisa.isEmpty() || godinaUpisa.length() > 4) {
            JOptionPane.showMessageDialog(this, "Unesite godinu upisa u ispravnom formatu!");
        } else if(fakultet == null) {
            JOptionPane.showMessageDialog(this, "Odaberite fakultet!");
        } else if(brIndeksa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite broj indeksa!");
        } else {
            student = new Student(0, ime, prezime, brIndeksa, godinaUpisa, false, fakultet);            
        }
        
        return student;
    }

    private void ocistiPolja() {
        jTextFieldBrIndeksa.setText("");
        jTextFieldGodinaUpisa.setText("");
        jTextFieldImeStudenta.setText("");
        jTextFieldPrezimeStudenta.setText("");
    }
