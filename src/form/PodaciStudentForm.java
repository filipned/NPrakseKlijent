    private void jButtonSacuvajStudentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajStudentaActionPerformed
        // TODO add your handling code here:      
        Student student = vratiStudenta();
        
        try {
            if(tipUnosa == "INSERT") {
                
                if(student != null) {
                    Controler.getInstance().unesiNovogStudenta(student);
                    JOptionPane.showMessageDialog(this, "Uspesno sacuvan student");
                    ocistiPolja();
                    mainForm.srediStudentTabelu();
                }
            } else {
                if(student != null) {
                    student.setStudentID(studentZaIzmjenu.getStudentID());
                    Controler.getInstance().azurirajStudenta(student);
                    JOptionPane.showMessageDialog(this, "Uspesno azuriran student");
                    dispose();
                    mainForm.srediStudentTabelu();
                }
            }
            
        } catch (IOException ex) {
            Logger.getLogger(PodaciStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PodaciStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(PodaciStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonSacuvajStudentaActionPerformed

